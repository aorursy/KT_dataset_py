import numpy as np # linear algebra
import pandas as pd # data processing, CSV file I/O (e.g. pd.read_csv)
import os
import seaborn as sns
import matplotlib.pyplot as plt
sns.set_style('whitegrid')
#print(os.listdir("../input"))
pd.read_csv('../input/beers.csv').head()
pd.read_csv('../input/breweries.csv').head()
df = pd.read_csv('../input/beers.csv')
df = df.drop(['Unnamed: 0', 'id'], axis=1)
bf = pd.read_csv('../input/breweries.csv')
df = pd.merge(df, bf, how='left', left_on='brewery_id', right_on='Unnamed: 0')
df = df.drop('Unnamed: 0', axis=1)

plt.figure(figsize=(15,5))
plt.title('Null Values')
ax = sns.heatmap(df.isnull().astype('int'), cmap="inferno")
plt.show()
plt.figure(figsize=(15,5))
plt.title('Number of Craft Beers per State')
ax = df.state.value_counts().plot()
plt.xticks(np.arange(len(df.state.unique())), df.state.value_counts().index, rotation=80)
plt.show()
plt.figure(figsize=(20,5))
plt.title('Alcohol By Volume By State')
ax = sns.boxplot(x="state", y="abv", data=df)
plt.xticks(np.arange(len(df.state.unique())), df.state.value_counts().index, rotation=80)
plt.show()
plt.figure(figsize=(10,5))
plt.title('Distribution of Beer Volume')
sns.boxplot(df.ounces)
plt.show()
plt.figure(figsize=(10,5))
plt.title('ABV vs Volume')
sns.swarmplot(x='ounces', y='abv', data=df)
plt.show()
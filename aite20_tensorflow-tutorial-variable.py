import tensorflow as tf
a = tf.Variable(8, dtype=tf.int64)
b = tf.Variable(9, dtype=tf.int64)
c = tf.Variable(7.8, dtype=tf.float64)
d = tf.Variable([[9, 4, 2],
                 [2, 1, 1]], dtype=tf.int64)
e = tf.Variable('Sup, blood!', dtype=tf.string)
with tf.Session() as session:
    session.run(tf.global_variables_initializer())
    print(f"a = {session.run(a)}")
    print(f"b = {session.run(b)}")
    print(f"c = {session.run(c)}")
    print(f"d = {session.run(d)}")
    print(f"e = {session.run(e)}")
    
    session.close()
price = tf.Variable(67.37, dtype=tf.float64)

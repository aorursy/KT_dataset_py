session.run(init)
print(f"The original price is: {session.run(price)}")
session.close()
price1 = tf.Variable(56.378, dtype=tf.float32)
price2 = tf.Variable(45.32, dtype=tf.float32)
price3 = tf.Variable(45, dtype=tf.int64)
price4 = tf.Variable(89.99, dtype=tf.float32)

with tf.Session() as session:
    init = tf.global_variables_initializer()
    session.run(init)
    print(f"First price: {session.run(price1)}")
    print(f"Second price: {session.run(price2)}")
    print(f"Third price: {session.run(price3)}")
    print(f"Fourth price is: {session.run(price4)}")
    
    session.close()
Name = tf.Variable(input('Enter your name: '), dtype=tf.string)
session = tf.Session()
session.run(tf.global_variables_initializer())
print(f"Name: {session.run(Name)}")
session.close()
Name = tf.assign(Name, 'Mark')
session = tf.Session()
print(f"The new name: {session.run(Name)}")
session.close()

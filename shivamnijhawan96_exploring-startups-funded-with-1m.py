import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
import numpy as np
plt.style.use('fivethirtyeight')
data = pd.read_csv('../input/startup_funding.csv')
data.shape
df = data[data['AmountInUSD']=='1,000,000']
df.drop(['Remarks','SNo'],axis=1,inplace=True)
df = df.reset_index()
del df['index']
df.head()
print('missing values in dataframe :')
df.isnull().sum()
plt.figure(figsize=(10,4))
df['CityLocation'].dropna(inplace=True)
sns.countplot(y='CityLocation', data=df, order=df['CityLocation'].value_counts().index,color='midnightblue')
plt.title('City wise Startups funded 1M')
plt.ylabel('')
plt.show()
df['InvestorsName'].dropna(inplace=True)
df.InvestorsName.replace(['Undisclosed investors'], ['Undisclosed Investors'], inplace=True)
top_investors = df['InvestorsName'].value_counts()[:10].to_frame()
plt.figure(figsize=(10,5))
sns.barplot(top_investors.index,top_investors['InvestorsName'],color='midnightblue')
plt.title('Top Investors')
plt.xlabel('')
plt.xticks(rotation=90)
plt.show()
from wordcloud import WordCloud
df['InvestorsName'].dropna(inplace= True)
df['InvestorsName'].replace('\s+', '_',regex=True,inplace=True)
investors_names = " ".join(df['InvestorsName'])
plt.figure(figsize=(14,6))
wordcloud = WordCloud(background_color='white', width=700,height=400 , max_words=40).generate(investors_names)
plt.imshow(wordcloud)
plt.axis("off")
plt.title("Investors who fund 1M")
plt.show()
df.InvestmentType.replace(['SeedFunding'], ['Seed Funding'], inplace=True)
df.InvestmentType.replace(['Crowd funding'], ['Crowd Funding'], inplace=True)
plt.figure(figsize=(8,8))
df.InvestmentType.value_counts().plot(kind='pie',autopct='%1.1f%%',colors=None)
plt.axis('equal')
plt.title('Types of Funding')
plt.ylabel('')
plt.xlabel('')
plt.show()
iv = df['IndustryVertical'].value_counts()[:10].to_frame()
plt.figure(figsize=(10,5))
sns.barplot(iv.index,iv['IndustryVertical'],color='midnightblue')
plt.title('Industry Vertical')
plt.xlabel('')
plt.xticks(rotation=90)
plt.show()
#Creates initial contingency table between Churn and International Plan
churn_crosstab = pd.crosstab(df2["Int'l Plan"], df2["Churn?"],margins=True)
display(churn_crosstab)
#Barplot/Stacked  of International Plan Use and Churn
churn_crosstab = pd.crosstab(df2["Int'l Plan"], df2["Churn?"])
